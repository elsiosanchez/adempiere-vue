<template>
  <el-select
    v-model="selectedFields"
    :filterable="true"
    :placeholder="$t('components.filterableItems')"
    multiple
    collapse-tags
    value-key="key"
    style="float: right;"
    @change="addField"
  >
    <el-option
      v-for="(item, key) in fieldListOptional"
      :key="key"
      :label="item.name"
      :value="item.columnName"
    />
  </el-select>
</template>

<script>
export default {
  name: 'FilterFields',
  props: {
    containerUuid: {
      type: String,
      required: true
    },
    panelType: {
      type: String,
      default: 'window'
    },
    groupField: {
      type: String,
      default: undefined
    },
    isFirstGroup: {
      type: Boolean,
      default: true
    }
  },
  data() {
    return {
      selectedFields: [], // fields optional showd
      fieldListOptional: []
    }
  },
  mounted() {
    this.getPanel()
  },
  methods: {
    isDisplayed(field) {
      var isMandatory = field.isMandatory || field.isMandatoryFromLogic
      var isDisplayed = field.isActive && field.isDisplayed && (field.isShowedFromUser || field.isDisplayedFromLogic || isMandatory)
      if (field.isShowedFromUser && field.isDisplayed) {
        this.selectedFields.push(field.columnName)
      }
      return isDisplayed
    },
    getPanel() {
      var fieldList = this.$store.getters.getFieldsListFromPanel(this.containerUuid)
      if (typeof fieldList === 'undefined' || fieldList.length === 0) {
        this.$store.dispatch('getPanelAndFields', {
          containerUuid: this.containerUuid,
          type: this.panelType.trim()
        }).then(response => {
          this.generatePanel(response.fieldList)
        }).catch(err => {
          console.warn('Field Load Error ' + err.code + ': ' + err.message)
        })
      } else {
        this.generatePanel(fieldList)
      }
    },
    generatePanel(fieldList) {
      this.fieldListOptional = fieldList.filter((item) => {
        if (!item.isMandatory && this.groupField === item.groupAssigned) {
          return this.isDisplayed(item)
        }
      })
    },
    /**
     * @param {array} selectedValues
     */
    addField(selectedValues) {
      this.$store.dispatch('changeFieldShowedFromUser', {
        containerUuid: this.containerUuid,
        fieldsUser: selectedValues,
        show: true
      })
    }
  }
}
</script>
