<template>
  <el-col v-if="items.children" :span="24">
    <el-collapse v-model="activeNames">
      <el-collapse-item :title="title" name="1" class="collapse-item">
        <el-row justify="space-around">
          <template v-for="(item, index) in items.children">
            <el-col :key="index" :span="isMobile()">
              <el-card :key="index" shadow="never" class="custom-card" :body-style="{ padding: '10px' }" @click.native="redirect(item)">
                <div class="icon-wrapper">
                  <svg-icon :icon-class="item.meta.icon" />
                </div>
                <div class="text-wrapper">
                  <p>{{ item.meta.title }}</p>
                </div>
              </el-card>
            </el-col>
          </template>
        </el-row>
      </el-collapse-item>
    </el-collapse>
  </el-col>
  <el-col v-else :span="isMobile()">
    <el-card shadow="never" class="custom-card" :body-style="{ padding: '10px' }" @click.native="redirect(items)">
      <div class="icon-wrapper">
        <svg-icon :icon-class="items.meta.icon" />
      </div>
      <div class="text-wrapper">
        <p>{{ items.meta.title }}</p>
      </div>
    </el-card>
  </el-col>
</template>

<script>
export default {
  name: 'Dropdown',
  props: {
    items: {
      type: Object,
      default: () => {
        return {}
      }
    },
    title: {
      type: String,
      default: ''
    }
  },
  data() {
    return {
      activeNames: ['1'],
      isActive: false
    }
  },
  computed: {
    device() {
      return this.$store.state.app.device
    }
  },
  methods: {
    redirect(item) {
      this.$router.push({ name: item.name, params: { childs: item.children }})
    },
    isMobile() {
      if (this.device === 'mobile') {
        return 24
      } else {
        return 8
      }
    }
  }
}
</script>

