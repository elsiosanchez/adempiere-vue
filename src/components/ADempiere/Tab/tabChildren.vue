<template>
  <el-tabs v-model="currentTab" type="border-card" @tab-click="handleClick">
    <template v-for="(item, key) in tabsList">
      <!-- TODO: Add support to tabs isSortTab (sequence) -->
      <el-tab-pane
        v-if="!item.isSortTab && !item.isTranslationTab"
        :key="key"
        :label="item.name"
        :windowuuid="windowUuid"
        :tabuuid="item.uuid"
        :position-tab="key"
        :name="String(key)"
        :lazy="true"
        :disabled="isCreateNew"
      >
        <el-col :span="24">
          <data-table
            :parent-uuid="windowUuid"
            :container-uuid="item.uuid"
            :panel-type="panelType"
          />
        </el-col>
      </el-tab-pane>
    </template>
  </el-tabs>
</template>

<script>
import { tabMixin } from '@/components/ADempiere/Tab/tabMixin'
import DataTable from '@/components/ADempiere/DataTable'

export default {
  name: 'TabChildren',
  components: {
    DataTable
  },
  mixins: [tabMixin],
  props: {
    firstTabUuid: {
      type: String,
      default: undefined
    }
  },
  computed: {
    getterIsLoadRecordParent() {
      return this.$store.getters.getDataRecordAndSelection(this.firstTabUuid).isLoaded
    },
    getDataSelection() {
      return this.$store.getters.getDataRecordAndSelection(this.tabUuid)
    }
  },
  watch: {
    getDataSelection(value) {
      if (this.getterIsLoadRecordParent && !value.isLoaded) {
        this.getData()
      }
    }
  }
}
</script>

